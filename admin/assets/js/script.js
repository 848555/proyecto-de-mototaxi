// Esperar 5 segundos y luego ocultar los mensajes
setTimeout(function() {
    var messages = document.querySelectorAll('.alert-message');
    
    messages.forEach(function(message) {
        message.style.display = 'none';
    });
}, 5000); // 5000 milisegundos = 5 segundos

